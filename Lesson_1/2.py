# 2. Выполнить логические побитовые операции "И", "ИЛИ" и др.
# над числами 5 и 6. Выполнить
# над числом 5 побитовый сдвиг вправо и влево на два знака.

print("Результат побитового OR : %10s" % bin(5 | 6))

print("Результат побитового OR : %10s" % bin(5 | 6))
print("Результат побитового AND: %10s" % bin(5 & 6))

print("сдвиг влево на два знака  : %10s" % bin(5 << 2))
print("сдвиг вправо на два знака : %6s" % bin(5 >> 2))
